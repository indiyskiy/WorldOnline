package model.additionalentity.admin;

public class SimpleTagGroup {
    private long id;
    private String text;

    public void setID(long id) {
        this.id = id;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }
}
